package com.Dashboard;

public class MostImportantDoctors {

    String name;
    String category;
    double score;
    int totalPatients;
    double percentageAppointments;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCategory() {
        return category;
    }

    public void setCategory(String category) {
        this.category = category;
    }

    public double getScore() {
        return score;
    }

    public void setScore(double score) {
        this.score = score;
    }

    public int getTotalPatients() {
        return totalPatients;
    }

    public void setTotalPatients(int totalPatients) {
        this.totalPatients = totalPatients;
    }

    public double getPercentageAppointments() {
        return percentageAppointments;
    }

    public void setPercentageAppointments(double percentageAppointments) {
        this.percentageAppointments = percentageAppointments;
    }
}
